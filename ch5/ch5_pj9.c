#include <stdio.h>

int main(void)
{
    // 1. 초기 연산 및 음수 표현 확인
    printf("%9x\n", -1);          // ffffffff (모든 비트가 1)
    printf("%3d\n", 10 & -1);     // 10 (1010 & 1111 = 1010)
    printf("%3d\n\n", 10 | 0);    // 10 (1010 | 0000 = 1010)

    // 2. 변수 x(15)를 이용한 비트 조작
    int x = 15;                   // 2진수: 1111

    // %08x는 16진수를 8자리로 출력하되 빈곳은 0으로 채우라는 뜻입니다.
    printf("%3d %08x\n", x, x);         // 15 0000000f

    // AND 연산: 마지막 비트 추출 (홀수/짝수 판별)
    printf("%3d %08x\n", x & 1, x & 1); //  1 00000001

    // OR 연산: 비트 켜기
    printf("%3d %08x\n", x | 1, x | 1); // 15 0000000f

    // XOR 연산: 비트 반전 (다르면 1)
    printf("%3d %08x\n", x ^ 1, x ^ 1); // 14 0000000e

    // NOT 연산: 전체 비트 뒤집기
    printf("%3d %08x\n", ~x, ~x);       // -16 fffffff0

    return 0;
}